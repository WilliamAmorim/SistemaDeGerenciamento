    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(Jtable_usuarios.getSelectedRow() != -1){
            UsuariosRequisitanteCadastroGUI abrir = new UsuariosRequisitanteCadastroGUI();
            this.setEnabled(false);
            abrir.telaPai = this;
            abrir.codigo = Jtable_usuarios.getValueAt(Jtable_usuarios.getSelectedRow(), 0).toString();
            abrir.nome = Jtable_usuarios.getValueAt(Jtable_usuarios.getSelectedRow(), 1).toString();
            abrir.cpf = Jtable_usuarios.getValueAt(Jtable_usuarios.getSelectedRow(), 2).toString();
            abrir.update = true;
            abrir.codigoCarregar();
            abrir.setVisible(true);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

